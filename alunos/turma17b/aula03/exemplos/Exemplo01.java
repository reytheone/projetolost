package exemplos;

public class Exemplo01 {
    public static void main(String[] args) {
        int cont;

        cont = 1; // inicialização da variável

        while (cont <= 12) {
            System.out.println(cont);
            cont++; // cont = cont +1; // Passo

        }

        System.out.println("FIM - " + cont);
    }
}
