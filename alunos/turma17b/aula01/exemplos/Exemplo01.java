package exemplos;

/*
 * Comnetário de bloco
 * Atalhos:
 * class - bloco de classe
 * main - bloco main
 * syso - comando print
 */

public class Exemplo01 {

    public static void main(String[] args) {
        System.out.println("Hello World!"); // println - pula para a próxima linha
        System.out.print("Teste!"); // print - não pula a linha
        System.out.println("Teste2");
    }
}